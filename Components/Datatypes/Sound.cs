namespace Escape_Room
{
    /// <summary>
    /// Represents a single sound with a specific frequency and duration.
    /// </summary>
    public struct Sound
    {
        /// <summary>
        /// Frequency of the sound in Hertz.
        /// </summary>
        public int Frequency;

        /// <summary>
        /// Duration of the sound in milliseconds.
        /// </summary>
        public int Duration;
    }
}
